#1. Input value = 10C | Expected output = 50F

print("="*20+"TEMPERATUR CONVERTER"+"="*20)

print("1. Convert from Celcius to Fahreheit")
print("2. Convert from Fahrenheit to Celcius")

option = input("Choose an option -->> 1 or 2 :")

if option == "1" :
    celcius = float(input("Type a temperature in degrees Celcius :"))
    fahrenheit = (celcius * 9/5) + 32
    print(f"Temperature {fahrenheit} degrees Fahrenheit")
elif option == "2" :
    fahrenheit = float(input("Type a temperature in degrees Celcius :"))
    celcius = (fahrenheit - 32) * 5/9
    print(f"Temperature {celcius} degrees Celcius")
else :
    print("Invalid option")

print("="*50+"PROGRAM END!"+"="*50)

