#MODULE FUNGSI LUAS

#LUAS SEGITIGA : 
def luas_segitiga(a, t):
    '''a = alas
    t = tinggi'''
    print("Luas Segitiga =", 0.5*a*t)

#LUAS PERSEGI PANJANG :
def luas_persegipanjang(p, l):
    '''p = panjang
    l = lebar'''
    print("Luas Persegi Panjang =", p*l)

#LUAS JAJAR GENJANG
def luas_jajargenjang(a, t):
    '''a = alas
    t = tinggi'''
    print("Luas Jajar Genjang =", a*t)
    
#LUAS LINGKARAN
def luas_lingkaran(r):
    '''r = jari-jari'''
    print("Luas Lingkaran =", 3.14 * (r**2))